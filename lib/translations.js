function getNestedTranslationValue(source, keys) {
  let value = source
  for (const key of keys) {
    if (value && Object.prototype.hasOwnProperty.call(value, key)) {
      value = value[key]
    } else {
      return undefined
    }
  }
  return value
}

// Helper function to get translation
export const t = (key, language = 'en') => {
  const keys = key.split('.')
  const normalizedLanguage = translations[language] ? language : 'en'
  const fallbackOrder =
    normalizedLanguage === 'it'
      ? ['it', 'en', 'cs']
      : normalizedLanguage === 'cs'
        ? ['cs', 'en', 'it']
        : ['en', 'it', 'cs']

  for (const lang of fallbackOrder) {
    const value = getNestedTranslationValue(translations[lang], keys)
    if (value !== undefined) {
      return value
    }
  }

  return key
}

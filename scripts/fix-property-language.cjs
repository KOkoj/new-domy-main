function fixCs(text) {
  let next = text
  for (const [from, to] of csWords) next = replaceWord(next, from, to)
  next = next
    .replace(/kuchyňě/g, 'kuchyně')
    .replace(/kuchyňí/g, 'kuchyní')
    .replace(/coz/g, 'což')
    .replace(/mit/g, 'mít')
    .replace(/more/g, 'moře')
    .replace(/zeleni kopcu/g, 'zeleni kopců')
    .replace(/do zeleni/g, 'do zeleně')
    .replace(/v zelene a/g, 'v zelené a')
    .replace(/Citta di Castello/g, 'Città di Castello')
    .replace(/\bpripravené\b/g, 'připravené')
    .replace(/\bk okamzitemu\b/g, 'k okamžitému')
    .replace(/\bpro turisticke\b/g, 'pro turistické')
    .replace(/\bvyuziti\b/g, 'využití')
    .replace(/\bm2\b/g, 'm2')
  return next
}

function fixIt(text) {
  let next = text
  for (const [pattern, replacement] of itRegexReplacements) {
    next = next.replace(pattern, replacement)
  }
  return next
}

function fixEn(text) {
  let next = text
  for (const [pattern, replacement] of enRegexReplacements) {
    next = next.replace(pattern, replacement)
  }
  next = next.replace(/\bCitta di Castello\b/g, 'Città di Castello')
  next = next.replace(/\bLocalita\b/g, 'Località')
  return next
}

function fixCommon(text) {
  return text
    .replace(/\bCitta di Castello\b/g, 'Città di Castello')
    .replace(/\bcitta di castello\b/g, 'città di castello')
    .replace(/\bLocalita\b/g, 'Località')
    .replace(/\blocalita\b/g, 'località')
    .replace(/\bnuda proprieta\b/g, 'nuda proprietà')
    .replace(/\bproprieta plurifamiliare\b/g, 'proprietà plurifamiliare')
}

function fixStringByKey(key, value) {
  if (typeof value !== 'string') return value
  const common = fixCommon(value)
  if (/_cs$/.test(key) || key === 'cs') return fixCs(common)
  if (/_it$/.test(key) || key === 'it') return fixIt(common)
  if (/_en$/.test(key) || key === 'en') return fixEn(common)
  return common
}

function fixObject(value, key = '') {
  if (Array.isArray(value)) return value.map((item) => fixObject(item, key))
  if (value && typeof value === 'object') {
    const out = {}
    for (const [childKey, childValue] of Object.entries(value)) {
      out[childKey] = fixObject(childValue, childKey)
    }
    return out
  }
  return fixStringByKey(key, value)
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, ''))
}

function writeJson(filePath, value) {
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, 'utf8')
}

function collectListingFiles() {
  const files = []
  for (const entry of fs.readdirSync(importRoot, { withFileTypes: true })) {
    if (!entry.isDirectory() || entry.name.startsWith('_')) continue
    const listingPath = path.join(importRoot, entry.name, 'listing.json')
    if (fs.existsSync(listingPath)) files.push(listingPath)
  }
  return files
}

let changed = 0

for (const filePath of [localPropertiesPath, ...collectListingFiles()]) {
  const before = fs.readFileSync(filePath, 'utf8')
  const afterObject = fixObject(JSON.parse(before.replace(/^\uFEFF/, '')))
  const after = `${JSON.stringify(afterObject, null, 2)}\n`
  if (after !== before) {
    fs.writeFileSync(filePath, after, 'utf8')
    changed += 1
  }
}


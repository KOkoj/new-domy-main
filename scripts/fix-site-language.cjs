function replaceWord(text, from, to) {
  return text.replace(new RegExp(`\\b${from}\\b`, 'g'), to)
}

function fixCzech(text) {
  let next = text
  for (const [from, to] of csReplacements) next = replaceWord(next, from, to)
  return next
}

function fixItalian(text) {
  let next = text
  for (const [pattern, replacement] of italianReplacements) next = next.replace(pattern, replacement)
  return next
}

function fixLine(line, state) {
  const isCzechLine =
    /cs\s*:/.test(line) ||
    /language\s*===\s*['"]cs['"]/.test(line) ||
    state.inCzechArray
  const startsCzechArray = /cs\s*:\s*\[/.test(line)
  const endsArray = state.inCzechArray && /\]/.test(line)

  if (startsCzechArray) state.inCzechArray = true

  let next = fixItalian(line)
  if (isCzechLine || startsCzechArray) next = fixCzech(next)

  if (endsArray) state.inCzechArray = false
  return next
}

let changed = 0

for (const rel of files) {
  const full = path.join(root, rel)
  if (!fs.existsSync(full)) continue

  const before = fs.readFileSync(full, 'utf8')
  const state = { inCzechArray: false }
  const after = before
    .split(/\r?\n/)
    .map((line) => fixLine(line, state))
    .join('\n')

  if (after !== before) {
    fs.writeFileSync(full, after, 'utf8')
    changed += 1
  }
}

console.log(`Site language cleanup completed. Changed files: ${changed}`)
